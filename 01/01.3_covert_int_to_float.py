# To convert a float to an integer specify that it is an integer, then add the float in ()
# The printed output will be the decimal number with just the float chopped off, it doesn't rount up or down

int_amount = int(10.4)
print(int_amount)



# Likewise, changing an integer to a float, will only add .0, as it represents a whole number still

float_amount = float(10)
print(float_amount)